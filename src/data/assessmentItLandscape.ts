export function getReadinessLevel(totalScore: number) {
  return (
    readinessLevels.find(
      (level) => totalScore >= level.min && totalScore <= level.max,
    ) ?? readinessLevels[0]
  );
}

export type RadarAxisPoint = {
  axis: string;
  value: number;
};

export function buildRadarData(scoresByQuestion: number[]): RadarAxisPoint[] {
  return itLandscapeQuestions.map((q, idx) => ({
    axis: q.dimension,
    value: scoresByQuestion[idx] ?? 1,
  }));
}

export function getProfileLabel(id: ProfileQuestion["id"], value: string): string {
  const question = profileQuestions.find((q) => q.id === id);
  const option = question?.options.find((o) => o.value === value);
  return option?.text ?? value;
}

export function scoreColor(score: number): string {
  if (score <= 1) return "#e30613";
  if (score <= 2) return "#d7a21f";
  if (score <= 3) return "#2ea36b";
  return "#7b5cff";
}
