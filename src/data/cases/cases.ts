export function getTechCampCase(slug: string) {
  return techCampCases.find((item) => item.slug === slug);
}

export function getAdjacentTechCampCases(slug: string) {
  const index = techCampCases.findIndex((item) => item.slug === slug);
  if (index === -1) return { prev: undefined, next: undefined };
  return {
    prev: index > 0 ? techCampCases[index - 1] : undefined,
    next: index < techCampCases.length - 1 ? techCampCases[index + 1] : undefined,
  };
}
